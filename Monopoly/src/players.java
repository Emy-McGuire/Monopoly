
public class players
	{

		private double money;
		private String name;
		public players (double m, String n)
		{
		money = m;
		name = n;
		}
		public double getMoney()
		{
		return money;
		}
		public void setMoney(double money)
		{
		this.money = money;
		}
		public String getName()
		{
		return name;
		}
		public void setName(String name)
		{
		this.name = name;
		}
	
	}
